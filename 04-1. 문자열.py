# 문자열 작성 (작은따옴표)
sentence = '나는 소년입니다'
print(sentence)

# 문자열 작성 (큰따옴표)
sentence2 = "파이썬은 쉬워요"
print(sentence2)

# 여러 줄에 걸쳐 문자열 작성
sentence3 = """
나는 소년이고,
파이썬은 쉬워요.
"""
print(sentence3)

# 출력 결과물
# 나는 소년입니다
# 파이썬은 쉬워요

# 나는 소년이고,
# 파이썬은 쉬워요.

# 문자열 내에 따옴표가 존재할 경우
sentence4 = '소년이 말했다. "나는 소년입니다."'
print(sentence4)

sentence5 = "소년이 생각했다. '파이썬은 쉽다.'"
print(sentence5)

sentence6 = """
소년이 말했다. "나는 소년입니다."
소년이 생각했다. '파이썬은 쉽다.'
"""
print(sentence6)